public class EVA_13_CICLO_FOR {

    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println("----------------------------------------------------");
        for(int i = 9; i >= 0; i--) {
            System.out.println(i);
            
            }
        for (int i = 0; i <= 100; i+=2) {
            System.out.print(i + "-");
            
            
        }
        
        
        
        
        
    }
}
